package outros;

public class Contador 
{
	private int numero;
	
	public int getNumero()
	{
		return this.numero;
	}
	
	public void setNumero(int numero)
	{
		this.numero = numero;
	}
	
	public void incrementarNumero()
	{
		this.numero++;
	}
	
	public void decrementarNumero()
	{
		this.numero--;
	}
}